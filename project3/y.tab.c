ret_union* lookup(char *id) {
    symbol *temp = head;
    symbol *result = 0;
    while (temp != 0) {
        if (strcmp(temp->id, id) == 0) {
            // find last one in the same scope
            result = temp;
            strcpy(result->unionVal.data.idVal, id);
        }
        temp = temp->next;
    }
    if (result == 0) {
        return 0;
    }
    return &(result->unionVal);
}

